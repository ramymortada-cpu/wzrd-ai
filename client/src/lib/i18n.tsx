export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem("wzrd-locale");
    const initialLocale = (saved === "en" || saved === "ar") ? saved : "ar";
    return initialLocale;
  });

  const dir: Dir = locale === "ar" ? "rtl" : "ltr";

  const setLocale = useCallback((newLocale: Locale) => {
    setLocaleState(newLocale);
    localStorage.setItem("wzrd-locale", newLocale);
  }, []);

  const toggleLocale = useCallback(() => {
    setLocale(locale === "en" ? "ar" : "en");
  }, [locale, setLocale]);

  const t = useCallback(
    (key: string) => {
      return translations[locale][key] || translations["en"][key] || key;
    },
    [locale]
  );

  useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = locale;
    if (locale === "ar") {
      document.documentElement.classList.add("rtl");
    } else {
      document.documentElement.classList.remove("rtl");
    }
  }, [locale, dir]);

  return (
    <I18nContext.Provider value={{ locale, dir, t, setLocale, toggleLocale }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}

export function useDir() {
  const { dir } = useI18n();
  return dir;
}
